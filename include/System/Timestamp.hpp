#ifndef QUE_SYSTEM_TIMESTAMP_HPP
#define QUE_SYSTEM_TIMESTAMP_HPP

#include <Build.hpp>
#include <string>

#ifndef QUE_PLATFORM_WINDOWS
#error Not a windows system. Function Now uses localtime_s, which is not the same for other systems.
#endif

namespace Que
{

	////////////////////////////////////////////////////////////////
	/// \ingroup System
	/// \brief Timestamp class
	///
	/// Day and month is 0 indexed.
	///
	////////////////////////////////////////////////////////////////
	class Timestamp
	{

	public:

		////////////////////////////////////////////////////////////////
		/// \brief Default constructor
		///
		////////////////////////////////////////////////////////////////
		Timestamp();

		////////////////////////////////////////////////////////////////
		/// \brief Constructor
		///
		////////////////////////////////////////////////////////////////
		Timestamp(	const unsigned short	p_Year,
					const unsigned char		p_Month,
					const unsigned char		p_Day,
					const unsigned char		p_Hours,
					const unsigned char		p_Minutes,
					const unsigned char		p_Seconds,
					const unsigned int		p_Microseconds);

		////////////////////////////////////////////////////////////////
		/// \brief Set the year.
		///
		////////////////////////////////////////////////////////////////
		void SetYear(const unsigned short p_Year);

		////////////////////////////////////////////////////////////////
		/// \brief Set the month.
		///
		////////////////////////////////////////////////////////////////
		void SetMonth(const unsigned char p_Month);

		////////////////////////////////////////////////////////////////
		/// \brief Set the day.
		///
		////////////////////////////////////////////////////////////////
		void SetDay(const unsigned char p_Day);

		////////////////////////////////////////////////////////////////
		/// \brief Set the hour.
		///
		////////////////////////////////////////////////////////////////
		void SetHour(const unsigned char p_Hours);

		////////////////////////////////////////////////////////////////
		/// \brief Set the minute.
		///
		////////////////////////////////////////////////////////////////
		void SetMinute(const unsigned char p_Minutes);

		////////////////////////////////////////////////////////////////
		/// \brief Set the second.
		///
		////////////////////////////////////////////////////////////////
		void SetSecond(const unsigned char p_Seconds);

		////////////////////////////////////////////////////////////////
		/// \brief Set the microsecond.
		///
		////////////////////////////////////////////////////////////////
		void SetMicrosecond(const unsigned int p_Microseconds);

		////////////////////////////////////////////////////////////////
		/// \brief Get the year.
		///
		////////////////////////////////////////////////////////////////
		int GetYear() const;

		////////////////////////////////////////////////////////////////
		/// \brief Get the month.
		///
		////////////////////////////////////////////////////////////////
		int GetMonth() const;

		////////////////////////////////////////////////////////////////
		/// \brief Get the day.
		///
		////////////////////////////////////////////////////////////////
		int GetDay() const;

		////////////////////////////////////////////////////////////////
		/// \brief Get the hour.
		///
		////////////////////////////////////////////////////////////////
		int GetHour() const;

		////////////////////////////////////////////////////////////////
		/// \brief Get the minute.
		///
		////////////////////////////////////////////////////////////////
		int GetMinute() const;

		////////////////////////////////////////////////////////////////
		/// \brief Get the second.
		///
		////////////////////////////////////////////////////////////////
		int GetSecond() const;

		////////////////////////////////////////////////////////////////
		/// \brief Get the microsecond.
		///
		////////////////////////////////////////////////////////////////
		int GetMicrosecond() const;

		////////////////////////////////////////////////////////////////
		/// \brief Validate the timestamp.
		///
		/// \return True if valid, else false.
		///
		////////////////////////////////////////////////////////////////
		bool Validate() const;

		////////////////////////////////////////////////////////////////
		/// \brief Check if the year is a leap year.
		///
		////////////////////////////////////////////////////////////////
		bool IsLeapYear() const;

		////////////////////////////////////////////////////////////////
		/// \brief Static function for getting the timestap of right now.
		///
		////////////////////////////////////////////////////////////////
		static Timestamp Now();

	private:

		// Private variables
		unsigned short		m_Year;
		unsigned char		m_Month;
		unsigned char		m_Day;
		unsigned char		m_Hours;
		unsigned char		m_Minutes;
		unsigned char		m_Seconds;
		unsigned int		m_Microseconds;

	};

}

#endif